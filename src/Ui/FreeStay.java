    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String rnum = jTextField2.getText(); // 입력받은 객실 수
        this.rnum = Integer.parseInt(rnum);
        int c = jList1.getAnchorSelectionIndex(); // 객실 선택 번호

        this.stayPrice = st.getRoompice(c, this.rnum) * FreeShedule.days; //일정에 따른 객실 가격
        this.freemoney = stayPrice + si.getFreeMoney(); // 자유여행 총합 가격

        jTextField1.setText(stayList[c]);
        jTextField3.setText(stayPrice + "원");
    }//GEN-LAST:event_jButton4ActionPerformed

