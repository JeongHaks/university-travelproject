    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int c = jList1.getAnchorSelectionIndex(); // 객실 선택 번호
            this.stayPrice = st.getRoompice(c, this.rnum) * FreeShedule.days; //일정에 따른 객실 가격
            this.freemoney = stayPrice + si.getFreeMoney(); // 자유여행 총합 가격

            jTextField1.setText(stayList[c]);
            jTextField3.setText(stayPrice + "원");

        }
    }//GEN-LAST:event_jList1MouseClicked

